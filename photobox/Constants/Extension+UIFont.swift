import UIKit

extension UIFont {
    static let xs = UIFont.systemFont(ofSize: 13)
    static let sm = UIFont.systemFont(ofSize: 14)
    static let md = UIFont.systemFont(ofSize: 16)
    static let lg = UIFont.systemFont(ofSize: 18)
    static let xl = UIFont.systemFont(ofSize: 20)
    static let xxl = UIFont.systemFont(ofSize: 32)
    
    static let xsBold = UIFont.systemFont(ofSize: 13, weight: .semibold)
    static let smBold = UIFont.systemFont(ofSize: 14, weight: .semibold)
    static let mdBold = UIFont.systemFont(ofSize: 16, weight: .semibold)
    static let lgBold = UIFont.systemFont(ofSize: 18, weight: .semibold)
    static let xlBold = UIFont.systemFont(ofSize: 20, weight: .semibold)
    static let xxlBold = UIFont.systemFont(ofSize: 32, weight: .semibold)
}
